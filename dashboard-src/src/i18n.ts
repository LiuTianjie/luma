type I18nKey = keyof typeof I18N.zh;

export function t(lang: Lang, key: I18nKey): string {
  return I18N[lang][key] || I18N.zh[key] || key;
}

export function localizeState(lang: Lang, value?: string): string {
  if (!value) return "-";
  const zh: Record<string, string> = {
    ready: "就绪",
    healthy: "健康",
    running: "运行",
    pending: "等待",
    failed: "失败",
    degraded: "降级",
    missing: "缺失",
    unknown: "未知",
  };
  return lang === "zh" ? zh[value] || value : value;
}
